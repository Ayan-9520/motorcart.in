export function greetingForSupplier(name: string): string {
  const h = new Date().getHours();
  const slot = h < 12 ? "morning" : h < 17 ? "afternoon" : "evening";
  const first = name.split(/\s+/)[0] || "Partner";
  return `Good ${slot} ${first} 👋`;
}

